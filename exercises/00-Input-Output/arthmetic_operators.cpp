
// Taking Input from the user ... The five Arithmetic Operators

#include <iostream>

using namespace std;

int main()
{
    int A, B;

    cout << "Please enter the first Number A\n";
    cin >> A;
    cout << "Please enter the second Number B\n";
    cin >> B;

    cout << "A + B = " << A + B << "\n";
    cout << "A - B = " << A - B << "\n";
    cout << "A * B = " << A * B << "\n";
    cout << "A / B = " << A / B << "\n";
    cout << "A % B = " << A % B << "\n\n";

    return 0;
}

