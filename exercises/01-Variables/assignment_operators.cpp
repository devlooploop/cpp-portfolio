
// Assignment Operators


#include <iostream>

using namespace std;

int main()
{
    int A = 10;
    int B = 20;

    A += B; // A = A + B
    cout << "A = " << A << endl;
    
    A -= B; // A = A - B
    cout << "A = " << A << endl;

    A *= B; // A = A * B
    cout << "A = " << A << endl;

    A /= B; // A = A / B
    cout << "A = " << A << endl;

    A %= B; // A = A % B
    cout << "A = " << A << endl;

    return 0;

}

























