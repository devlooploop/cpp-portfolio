
// Three Variables Total


#include <iostream>

using namespace std;

int main()
{
    int Num1=20, Num2=30, Num3=10;

    cout << Num1 << " +" << endl;
    cout << Num2 << " +" << endl;
    cout << Num3 << endl;
    cout << "------------------" << endl;
    cout <<"Total = " << Num1 + Num2 + Num3 << endl;

    return 0;
}






