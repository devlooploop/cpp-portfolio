// Math Functions in C++
// 

#include <iostream>
#include <cmath>

using namespace std;

int main()
{
	
	cout << round(2.4) << endl; // gives 2
	cout << round(2.5) << endl; // gives 3
	cout << round(2.7) << endl; // gives 3

	cout << sqrt(50) << endl; // gives 7.07107
	cout << round(sqrt(50)) << endl; // gives 7

	return 0;
}


