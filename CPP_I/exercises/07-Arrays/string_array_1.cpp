
// Example #1 String & Array

#include <iostream>
#include <string>

using namespace std;

int main()
{
    // String is an array of characters

    string x = "Jack Mahoni";

    cout << x[0] << endl; // Prints J 
    cout << x[2] << endl; // Prints c
    cout << x[7] << endl; // Prints h

    return 0;
}


