
// Example #2 String & Array

#include <iostream>
#include <string>

using namespace std;

int main()
{
    // String is an array of characters

    int x[5] = {22, 18, 2, 55, 520};

    cout << x[0] << endl; // Prints 22 
    cout << x[1] << endl; // Prints 18
    cout << x[2] << endl; // Prints 2
    cout << x[3] << endl; // Prints 55 
    cout << x[4] << endl; // Prints 520

    return 0;
}


